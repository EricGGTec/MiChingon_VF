    public void cargarRetirosEnTabla(JTable tabla) {
        String ruta = "retiros.txt";
        List<DatosTablaRetiro> listaRetiros = new ArrayList<>();

        try ( BufferedReader br = new BufferedReader(new FileReader(ruta))) {
            String linea;

            while ((linea = br.readLine()) != null) {
                String[] datos = linea.split(", ");  // separa por tabulación
                if (datos.length == 3) {
                    String nombre = datos[0];
                    int monto = Integer.parseInt(datos[1]);

                    // Intenta parsear la fecha
                    Date fecha;
                    try {
                        SimpleDateFormat sdf = new SimpleDateFormat("dd/MM/yyyy");
                        fecha = sdf.parse(datos[2]);
                    } catch (ParseException e) {
                        fecha = new Date(); // Si falla, asigna fecha actual como fallback
                    } catch (java.text.ParseException ex) {
                        fecha = new Date();
                    }
                    DatosTablaRetiro retiro = new DatosTablaRetiro(nombre, monto, LocalDate.now());
                    listaRetiros.add(retiro);
                }
            }
        } catch (IOException e) {
            e.printStackTrace();
        }

        // Cargar modelo personalizado
        MTablaRetiro modelo = new MTablaRetiro(listaRetiros);
        tabla.setModel(modelo);
    }

