    private void EliminarRetiroActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_EliminarRetiroActionPerformed
        // 3. Validación de selección en tabla
        int filaSeleccionada = TablaRetiros.getSelectedRow();
        if (filaSeleccionada < 0) {
            JOptionPane.showMessageDialog(null, "Seleccione una fila.");
            return;
        }

        if (!validarGerente()) {
            return;
        }

        // 4. Confirmación antes de eliminar
        int confirmacion = JOptionPane.showConfirmDialog(null, "¿Está seguro que desea eliminar este retiro?", "Confirmar", JOptionPane.YES_NO_OPTION);
        if (confirmacion != JOptionPane.YES_OPTION) {
            return;
        }

        // 5. Eliminar el retiro
        MTablaRetiro modelo = (MTablaRetiro) TablaRetiros.getModel();
        DatosTablaRetiro retiroAEliminar = modelo.getRetiroEnFila(filaSeleccionada);
        Retiros -= retiroAEliminar.getMonto(); // Ajustar total
        modelo.eliminarRetiro(filaSeleccionada);
        reescribirArchivoDesdeModelo(modelo);
        TablaRetiros.setModel(modelo);

        JOptionPane.showMessageDialog(null, "Retiro eliminado correctamente.");

    }//GEN-LAST:event_EliminarRetiroActionPerformed

    public boolean validarGerente() {
        boolean validar = false;
        // 1. Obtener la contraseña del gerente
        String contraseñaGerente = null;

        for (Trabajador t : trabajadores) {
            if (t.getRol().equalsIgnoreCase("Gerente")) {
                contraseñaGerente = t.getPassword();
                break;
            }
        }

        if (contraseñaGerente == null) {
            JOptionPane.showMessageDialog(null, "No se encontró al gerente en el sistema.");
            return validar;
        }
        // 2. Validar contraseña
        JPasswordField campoContrasena = new JPasswordField();
        int opcion = JOptionPane.showConfirmDialog(null, campoContrasena, "Contraseña Gerente", JOptionPane.OK_CANCEL_OPTION);

        if (opcion != JOptionPane.OK_OPTION) {
            return validar;
        }

        String ingresada = new String(campoContrasena.getPassword());
        if (!ingresada.equals(contraseñaGerente)) {
            JOptionPane.showMessageDialog(null, "Contraseña incorrecta.");
            return validar;
        }
        validar = true;
        return validar;
    }

    private void ModificarRetiroActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_ModificarRetiroActionPerformed
        // 1. Validar que se haya seleccionado una fila
        int filaSeleccionada = TablaRetiros.getSelectedRow();
        if (filaSeleccionada < 0) {
            JOptionPane.showMessageDialog(null, "Seleccione una fila.");
            return;
        }
        // 2. Obtener el retiro seleccionado
        MTablaRetiro modelo = (MTablaRetiro) TablaRetiros.getModel();
        DatosTablaRetiro retiroSeleccionado = modelo.getRetiroEnFila(filaSeleccionada);

        if (!validarGerente()) {
            return;
        }
        DatosTablaRetiro original = modelo.getRetiroEnFila(filaSeleccionada);
        // 3. Abrir el panel de edición con los datos
        InterfazEditarRetiro panel = new InterfazEditarRetiro(null, true, retiroSeleccionado);
        panel.setVisible(true);
        DatosTablaRetiro resultado = panel.getRetiroEditado();
        if (resultado != null) {
            Retiros = Retiros - original.getMonto() + resultado.getMonto(); // Reajustar total
            modelo.actualizarRetiro(filaSeleccionada, resultado);
            reescribirArchivoDesdeModelo(modelo);
            TablaRetiros.setModel(modelo);
        }

    }//GEN-LAST:event_ModificarRetiroActionPerformed

